    public void connectDB() { 
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            con = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=Rental;selectMethod=cursor", "sa", "123456");

            System.out.println("DATABASE NAME IS:" + con.getMetaData().getDatabaseProductName());

            Statement statement = con.createStatement();
            ResultSet resultSet = statement
                    .executeQuery("SELECT CustomerName, Address FROM CUSTOMER");
            
            while (resultSet.next()) {
                
                System.out.println("Customer Name and Address : " + 
                        resultSet.getString("CustomerName") + " -> " + resultSet.getString("Address"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
