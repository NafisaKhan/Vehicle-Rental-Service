    private void SignupButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignupButtonActionPerformed
        // TODO add your handling code here:
        name = nameField.getText();
        email = emailField.getText();
        phn = phoneField.getText();
        license = licenseField.getText();
        adrs = addressField.getText();
        pass = new String(passwordField.getPassword());
      
        if(name.equals("") || phn.equals("") || license.equals("") || pass.equals(""))
        {
            JOptionPane.showMessageDialog(this, "Please insert required information.");   
        }
        else
        {
            if(email.contains("@") == false)
            {
              JOptionPane.showMessageDialog(this, "Incorrect email address.");
            }
            else
            {
            try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            
            con = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=Rental;selectMethod=cursor", "sa", "123456");
           
            System.out.println("DATABASE NAME IS:" + con.getMetaData().getDatabaseProductName());
           
            //connectDB();
            pst = con.prepareStatement("INSERT INTO CUSTOMER(CustomerName, Phone, Password, DrivingLicense, Email, Address) values(?,?,?,?,?,?)");

            pst.setString(1, name);
            pst.setString(2, phn);
            pst.setString(3, pass);
            pst.setString(4, license);
            pst.setString(5, email);
            pst.setString(6, adrs);
           
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Thank you for signing up!");
           
            nameField.setText("");
            emailField.setText("");
            phoneField.setText("");
            licenseField.setText("");
            addressField.setText("");
            passwordField.setText("");
            nameField.requestFocus();
 
           //con.close();
           new UserProfilePage().setVisible(true);
           this.dispose();
           
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(SignUpPage.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(SignUpPage.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        }
    }//GEN-LAST:event_SignupButtonActionPerformed

